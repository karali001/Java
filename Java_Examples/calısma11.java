package javacalisma;

public class calısma11 {
	public static void main(String args[]) {
		System.out.println(Math.floor(3.2));//aşağıya yuvarlama
		System.out.println(Math.ceil(3.2));//yukarı yuvarlama
		System.out.println(Math.round(67.6));//yuvarlama
		System.out.println(Math.abs(-67.6));//mutlak alma
		System.out.println(Math.max(-67.6,2));//yuksek degeri alma
		System.out.println(Math.min(-67.6,2));// alcak degeri alma 
		System.out.println(Math.pow(2,3));//üst alma 2 **3 yapar
		System.out.println(Math.sqrt(16));//karekök alma
		System.out.println(Math.cbrt(64));// küp kök alma
		
	}

}
